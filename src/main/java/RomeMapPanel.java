        super.paintComponent(g);
        int w=getWidth(), h=getHeight();
        Graphics2D g2=(Graphics2D)g.create();
        g2.setRenderingHint(RenderingHints.KEY_ANTIALIASING, RenderingHints.VALUE_ANTIALIAS_ON);

        if(backgroundImage!=null) { g2.drawImage(backgroundImage,0,0,w,h,this); g2.setColor(new Color(0,0,0,140)); g2.fillRect(0,0,w,h); }
        else { g2.setColor(ThemeManager.get().palette().base.appBg); g2.fillRect(0,0,w,h); }

        // Full-screen map: occupy the entire panel, preserve the 1.861 aspect.
        // Title, blurb, legend, and nav buttons overlay directly on the map with
        // haloed text for contrast, the ornate frame is dropped so geography is the dominant element.
        // BBOX: lon [-12,55] lat [22,58] -> aspect = 67/36 = 1.861
        double aspect=1.861;
        int iw, ih;
        if((double)w/h > aspect) { ih=h; iw=(int)(ih*aspect); }
        else { iw=w; ih=(int)(iw/aspect); }
        int ix=(w-iw)/2, iy=(h-ih)/2;
        mapBounds=new Rectangle(ix,iy,iw,ih);

        boolean dark=ThemeManager.get().getCurrent()==Theme.DARK;

        Color fill  = dark ? new Color(65, 55, 40, 200) : new Color(215, 195, 160, 220);
        Color stroke = dark ? new Color(130, 110, 70)    : new Color(155, 125, 75);

        Shape oldClip = g2.getClip();
        g2.clipRect(ix, iy, iw, ih);

        Shape[] ss = new Shape[ALL_LANDS.length];
        for (int i = 0; i < ALL_LANDS.length; i++) ss[i] = createShape(ALL_LANDS[i], ix, iy, iw, ih);
        g2.setColor(new Color(40,80,130,40)); g2.setStroke(new BasicStroke(6f));
        for (Shape s : ss) g2.draw(s);
        g2.setColor(fill); for (Shape s : ss) g2.fill(s);
        g2.setColor(stroke); g2.setStroke(new BasicStroke(1.0f));
        for (Shape s : ss) g2.draw(s);

        g2.setClip(oldClip);

        // Labels
        Theme.Palette.Fonts f = ThemeManager.get().palette().fonts;
        Theme.Palette.Base b  = ThemeManager.get().palette().base;

        g2.setFont(f.caption); g2.setColor(new Color(100,140,180,dark?100:75));
        drawCentred(g2,"Western",       ix+(int)(0.18*iw),iy+(int)(0.56*ih));
        drawCentred(g2,"Mediterranean", ix+(int)(0.18*iw),iy+(int)(0.59*ih));
        drawCentred(g2,"Eastern",       ix+(int)(0.50*iw),iy+(int)(0.68*ih));
        drawCentred(g2,"Mediterranean", ix+(int)(0.50*iw),iy+(int)(0.71*ih));
        drawCentred(g2,"Adriatic",      ix+(int)(0.43*iw),iy+(int)(0.49*ih));
        drawCentred(g2,"Aegean",        ix+(int)(0.53*iw),iy+(int)(0.58*ih));
        drawCentred(g2,"Black Sea",     ix+(int)(0.64*iw),iy+(int)(0.38*ih));
        drawCentred(g2,"Atlantic",      ix+(int)(0.04*iw),iy+(int)(0.40*ih));
        drawCentred(g2,"North Sea",     ix+(int)(0.20*iw),iy+(int)(0.10*ih));
        drawCentred(g2,"Mare Nostrum",   ix+(int)(0.38*iw),iy+(int)(0.63*ih));

        g2.setColor(new Color(180,150,100,dark?70:55));
        drawCentred(g2,"Britannia",  ix+(int)(0.15*iw),iy+(int)(0.17*ih));
        drawCentred(g2,"Hispania",   ix+(int)(0.08*iw),iy+(int)(0.50*ih));
        drawCentred(g2,"Gallia",     ix+(int)(0.22*iw),iy+(int)(0.33*ih));
